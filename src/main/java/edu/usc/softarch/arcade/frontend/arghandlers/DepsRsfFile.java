package edu.usc.softarch.arcade.frontend.arghandlers;

import java.io.File;

/**
 * Argument Handler for the path of a Dependencies RSF file, typically of the
 * form *_deps.rsf.
 *
 * @author Marcelo Schmitt Laser
 */
public class DepsRsfFile
  extends ArgHandler
{
  //#region ATTRIBUTES
  private static ArgHandler depsRsfFile;
  //#endregion

  //#region CONSTRUCTORS
  private DepsRsfFile()
  {
    String name = "depsRsfFile";
    String description = "Dependencies RSF file";
    String instruction = "This is a file generated by a dependency builder, ";
    instruction += "usually of the form *_deps.rsf.";

    initialize(name, description, instruction);
  }

  public static ArgHandler getInstance()
  {
    if(depsRsfFile == null) depsRsfFile = new DepsRsfFile();
    return depsRsfFile;
  }
  //#endregion

  //#region VALIDATION
  @Override
  public boolean validateAsInput(String value)
    throws Exception
  {
    File depsFile = new File(value);
    if(!depsFile.exists())
    {
      String errorMessage = "Dependencies RSF file not found at given path: ";
      errorMessage += value;
      throw new Exception(value + " not found.");
    }
    return true;
  }

  @Override
  public boolean validateAsOutput(String value)
    throws Exception
  {
	  File depsRsfFile = new File(value);
    if((!depsRsfFile.getParentFile().exists())
      && (!depsRsfFile.getParentFile().mkdirs()))
        throw new Exception("Failed to create dependencies directory.");
    return true;
  }
  //#endregion
}
